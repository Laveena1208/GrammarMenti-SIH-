    <h1>1. Parts of Speech</h1>
    <p>
      <img src="images/parts_of_speech.png" width="550" height="300" /> While speaking,
      we make use of many words.We know very well that these words are divided
      into different classes according to their use. These word classes are
      known as parts of speech.<br />The 8 parts of speech are:<br />A. Verb
      &emsp;&emsp;&emsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; B. Noun<br />C.
      Adjectives &emsp;&emsp;&nbsp; D. Adverb<br />E. Pronoun &emsp;&emsp;&emsp;
      F. Conjuction<br />G. Preposition &emsp;&emsp;&nbsp;H. Interjection
    </p>
    <h2><b>A. Verb</b></h2>
    <p>
      <img src="images/Action-Verbs.png" width="500" height="500" /> <br /><br />
      &emsp;&emsp;&emsp;&emsp;&emsp; So now it's time to learn Verb!!!<br />You
      use sentences with many verbs daily. Let's see what does verb actually
      mean using few examples<br />
      <b>
        A verb is a word that defines action - an action word. It will tell what
        the subject of a sentence is doing or what will happen.</b
      ><br />Example A: "Terry ran upstairs." (ran tells what Terry did - Terry
      is the subject.)<br />
      Example B: "Katie eats her lunch." (eats tells what Katie is doing - Katie
      is the subject.)
    </p>
    <br /><br /><br /><br />
    <h2><b>B. Noun</b></h2>
    <p>
      <img src="images/noun.jpg" width="500" height="400" />
      <br />
      Look around you.Everything you can see, you can name. What do you see? A
      computer, room, mom, dad, fan, light...Before you commit this to memory, I
      would clarify one thing. Nouns are words that name people, places, things,
      or ideas.<br />
      Does that make sense? A noun is a word, not the actual thing that word
      represents. It's kind of cool? Now, task is memorize the following
      definition.<br />&emsp;&emsp;<b
        >A noun is a word, not the actual thing that word represents.</b
      >
    </p>
    <br /><br /><br />
    <h2><b>C. Adjective</b></h2>
    <p>
      <img src="images/Adjectives.jpg" width="500" height="350" />
      <br />
      Most students learn that adjectives are words that modify (describe)
      nouns. Adjectives do not modify verbs or adverbs or other adjectives.But
      adjectives can do more than just modify nouns. They can also act as a
      complement to linking verbs or the verb to be. <br /><b
        >Adjectives are words that describe the qualities or states of being of
        nouns</b
      ><br />Example:silly, yellow, fun, fast,etc..
    </p>
    <br /><br /><br />
    <h2><b>D. Adverb</b></h2>
    <p>
      <img src="images/adverb.jpg" width="500" height="350" /> Adverbs often modify
      verbs. This means that they describe the way an action is happening.<br />Example
      : Phillip sings loudly in the shower. My cat waits impatiently for his
      food.<br />How does Phillip sing? Loudly. How does my cat wait?
      Impatiently. Adverbs can answer other types of questions about how an
      action was performed.<br />
      <b
        >An adverb is a word that modifies a verb, an adjective, another adverb,
        or even a whole sentence.</b
      >
    </p>
    <br /><br />
    <h2><b>E. Pronoun</b></h2>
    <br />
    <p>
      <img src="images/Pronoun.jpg" width="500" height="350" /> You use pronouns every
      day. In fact, even if you don’t know what pronouns are, you use them—and
      in this sentence alone, we’ve now used pronouns four times. <br /><b
        >Pronouns are the words you substitute for other nouns when your reader
        or listener already knows which nouns you’re referring to.</b
      >For example, you might say, “I have a dog. He’s brown and white.” There’s
      no need to clarify that you’re describing your dog in the second sentence
      because you already mentioned him in the first.
    </p>
    <br /><br /><br />
    <h2><b>F. Conjunction</b></h2>
    <br />
    <p>
      <img src="images/conjunctions.jpg" width="500" height="350" /> Conjunctions allow
      you to form complex, elegant sentences and avoid the choppiness of
      multiple short sentences. So the definition of conjunction is :<br /><b
        >Conjunctions are words that link other words, phrases, or clauses
        together.</b
      ><br />
      Example: I like cooking and eating, but I don’t like washing dishes
      afterward. Sophie is clearly exhausted, yet she insists on dancing till
      dawn.
    </p>
    <br /><br /><br />
    <h2><b>G. Preposition</b></h2>
    <br />
    <p>
      <img src="images/preposition.jpg" width="500" height="350" />
      <b
        >A preposition is a word or group of words used before a noun, pronoun,
        or noun phrase to show direction, time, place, location, spatial
        relationships, or to introduce an object. </b
      ><br />To refer to one point in time, use the prepositions "in," "at," and
      "on."<br />To refer to a place, use the prepositions "in","at" ,"on" , and
      "inside"<br />To refer to a location, use the prepositions "in", "at" ,
      and "on" .
    </p>
    <br /><br /><br /><br /><br /><br />
    <h2><b>H. Interjection</b></h2>
    <br />
    <p>
      <img src="images/interjection.webp" width="500" height="350" />
      Interjection is a big name for a little word. Interjections are short
      exclamations like Oh!, Um or Ah! They have no real grammatical value but
      we use them quite often, usually more in speaking than in writing. When
      interjections are inserted into a sentence, they have no grammatical
      connection to the sentence. An interjection is sometimes followed by an
      exclamation mark (!) when written.
    </p>
